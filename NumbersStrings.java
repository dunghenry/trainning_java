class NumbersStrings {
    public static void main(String[] args) {
        String a = "10";
        int b = 20;
        System.out.println(a + b);

        // Special Characters
        String str = "Hello, \"world!\"";
        System.out.println(str);

        // \' : '
        // \" : "
        // \\ : \

        System.out.println("Hello\t" + "World");
    }
}